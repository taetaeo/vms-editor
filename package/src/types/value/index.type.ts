/**
 * @description 문자열 또는 숫자 형태
 */
export type StrOrNumType = string | number;

/**
 * @description 방향을 나타냄
 * - 왼쪽 (left)
 * - 오른쪽 (right)
 * - 위쪽 (top)
 * - 아래쪽 (bottom)
 */
export type DirectionType = "left" | "bottom" | "top" | "right" | "center";

/**
 * @description 수직 또는 수평을 나타냄
 * - 수직 (vertical)
 * - 수평 (horizon)
 */
export type AlignType = "horizon" | "vertical";
